    //proses menampilkan data tabel dari database
    protected void datatabel(){
        Object [] Baris = {"ID", "Nama Kendaraan", "Transmisi", "Jenis Kendaraan", "Transportasi", "Tahun", "Kisaran Harga"};
        tabmode = new DefaultTableModel(null, Baris);
        tblkendaraan.setModel(tabmode);
        String sql = "Select * From tbkendaraan";
        try {
            java.sql.Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()){
                String a = rs.getString("id");
                String b = rs.getString("nama");
                String c = rs.getString("transmisi");
                String d = rs.getString("jenis");
                String e = rs.getString("transportasi");
                String f = rs.getString("tahun");
                String g = rs.getString("harga");
                                
                String[] data={a,b,c,d,e,f,g};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }
    }

