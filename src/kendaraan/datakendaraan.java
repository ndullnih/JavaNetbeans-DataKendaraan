    private void btsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsimpanActionPerformed
        // TODO add your handling code here:
        String sql = "insert into tbkendaraan values (?,?,?,?,?,?,?)";
        try {
            PreparedStatement st = con.prepareStatement(sql);
            st.setString(1,tid.getText());
            st.setString(2,tnama.getText());
            
            //Komponen Radio Button
            String transmisi="";
            if (at.isSelected()) transmisi ="Matic";
            else transmisi ="Manual";
            st.setString(3, transmisi);
           
            //Komponmen ComboBox
            st.setString(4, cbjenis.getSelectedItem().toString());
            st.setString(5, cbtrans.getSelectedItem().toString());
            st.setString(6, ttahun.getText());
            st.setString(7, cbkisaran.getSelectedItem().toString());
       
            st.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            tid.requestFocus();
            datatabel();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan" +e);
        }
    }//GEN-LAST:event_btsimpanActionPerformed
